def state2array(state):
    array = [None] * 5
    for x in range(5):
        array[x] = [None] * 5
        for y in range(5):
            array[x][y] = [None] * 64
            tmp = 64 * (5 * y + x)
            for z in range(64):
                array[x][y][z] = ord(state[tmp + z]) - 48
    return array 


def array2state(array):
    state = [None] * 1600
    for x in range(5):
        for y in range(5):
            tmp = 64 * (5 * y + x)
            for z in range(64):
                state[tmp + z] = chr(array[x][y][z] + 48)
    return ''.join(state)



def theta(array):
    res = [None] * 5
    for x in range(5):
        res[x] = [None] * 5
        for y in range(5):
            res[x][y] = [None] * 64
            for z in range(64):
                tmp = array[x][y][z]
                for y1 in range(5):
                    tmp = tmp ^ array[(x - 1) % 5][y1][z] ^ array[(x + 1) % 5][y1][(z - 1) % 64]
                res[x][y][z] = tmp
    return res


