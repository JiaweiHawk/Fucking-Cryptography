    array = [None] * 5
    for x in range(5):
        array[x] = [None] * 5
        for y in range(5):
            array[x][y] = [None] * 64
            for z in range(64):
                array[x][y][z] = array_a[x][y][z] ^ array_b[x][y][z]

    return array


