def rho(array):
    for x in range(5):
        for y in range(5):              #实际为右移， 但为了实现改为循环左移
            tmp = array[x][y]
            array[x][y] = tmp[rho_offset[x][y]:] + tmp[:rho_offset[x][y]]
    return array


def pi(array):
    res = [None] * 5
    for x in range(5):
        res[x] = [None] * 5

    for x in range(5):
        for y in range(5):
            res[x][y] = array[(x + 3 * y) % 5][x]
    return res


def chi(array):
    res = [None] * 5
    for x in range(5):
        res[x] = [None] * 5
        for y in range(5):
            res[x][y] = [None] * 64
            for z in range(64):
                res[x][y][z] = array[x][y][z] ^ (~array[(x + 1) % 5][y][z]) & array[(x + 2) % 5][y][z]
    return res

